package backjoon;

import java.util.Scanner;

public class Main10869 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int first = sc.nextInt();
        int last = sc.nextInt();

        System.out.println(first+last);
        System.out.println(first-last);
        System.out.println(first*last);
        System.out.println(first/last);
        System.out.println(first%last);
    }
}
